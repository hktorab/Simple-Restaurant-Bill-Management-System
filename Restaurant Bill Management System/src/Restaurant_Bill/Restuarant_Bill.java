    private void jBtnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEqualActionPerformed

        try {
            if (lastOperator.equals("+")) {
                System.out.println("+");
                String enterNumber = jtextCalculatorDisplay.getText();
                double currentValue = Double.valueOf(enterNumber);
                calculatorFinalCalculate = calculatorFinalCalculate + currentValue;
                jtextCalculatorDisplay.setText("" + calculatorFinalCalculate);

            } else if (lastOperator.equals("-")) {
                System.out.println("-");
                String enterNumber = jtextCalculatorDisplay.getText();
                double currentValue = Double.valueOf(enterNumber);
                calculatorFinalCalculate = calculatorFinalCalculate - currentValue;
                jtextCalculatorDisplay.setText("" + calculatorFinalCalculate);
            } else if (lastOperator.equals("*")) {
                System.out.println("*");
                String enterNumber = jtextCalculatorDisplay.getText();
                double currentValue = Double.valueOf(enterNumber);
                calculatorFinalCalculate = calculatorFinalCalculate * currentValue;
                jtextCalculatorDisplay.setText("" + calculatorFinalCalculate);
            } else if (lastOperator.equals("/")) {
                System.out.println("/");
                String enterNumber = jtextCalculatorDisplay.getText();
                double currentValue = Double.valueOf(enterNumber);
                calculatorFinalCalculate = calculatorFinalCalculate / currentValue;
                jtextCalculatorDisplay.setText("" + calculatorFinalCalculate);
            } else {
                String enterNumber = jtextCalculatorDisplay.getText();
                jtextCalculatorDisplay.setText(enterNumber);
            }

            lastOperator = "";

        } catch (Exception e) {
            System.out.println(e);

            e.printStackTrace();

        }
    }//GEN-LAST:event_jBtnEqualActionPerformed

