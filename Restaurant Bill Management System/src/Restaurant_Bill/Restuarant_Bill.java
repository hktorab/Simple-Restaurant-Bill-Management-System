    private void jButtonTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTotalActionPerformed
        // TODO add your handling code here:
        double chicken = 0;
        double chikenAndMeal = 0;
        double baconAndBurger = 0;
        
        //Meals Details Setting
        if (checkBoxChicken.isSelected()) {

            chicken = 90 * Double.valueOf(jTextFieldChicketCheckBox.getText());
            costOFMeal += chicken;
            lunchItemChicken="Chicken : "+jTextFieldChicketCheckBox.getText()+" * 90 = "+ chicken;
            
        }

        if (checkBoxChickentAndMeal.isSelected()) {
            chikenAndMeal = 150 * Double.valueOf(jTextFieldChickentAndMeal.getText());
            costOFMeal += chikenAndMeal;
        lunchItemChickenAndMeal="Chicken And Meal :"+jTextFieldChicketCheckBox.getText()+" * 150 = "+chikenAndMeal ;
            
        }
        if (CheckBoxBaconAndBurger.isSelected()) {
            baconAndBurger = 275 * Double.valueOf(jTextFieldBaconAndBurger.getText());
            costOFMeal += baconAndBurger;
         lunchItemBacon="Bacon And Burger :"+jTextFieldChicketCheckBox.getText()+" * 275 = "+baconAndBurger;
            
        }
        
        double totalCostOfMeal = chicken + chikenAndMeal + baconAndBurger;
        jTextFieldCostOfMeals.setText("" + costOFMeal);

        double subtotalBill = costOfDrink + costOFMeal + delevary;
        jTextFieldSubTotal.setText("" + subtotalBill);
        if (!(jCheckBoxTax.isSelected())) {
            jTextFieldAllTotal.setText("" + subtotalBill);
        } else {
       tax = subtotalBill * 0.15;
            jTextFieldTax.setText("" + tax);

            double totalBill = subtotalBill + tax;
            jTextFieldAllTotal.setText("" + totalBill);

        }
        //total bill calculate


    }//GEN-LAST:event_jButtonTotalActionPerformed

